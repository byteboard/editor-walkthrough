import math


class MathStuff:
    """
    MathStuff
    ------------------
    This class contains some fun math stuff.
    """

    def add(x, y):
        """
        add(x, y)
        ------------------
        Return sum of x and y
        """
        return x + y

    def square(x):
        """
        square(x)
        ------------------
        Returns square of x
        """
        return math.pow(x, 2)
